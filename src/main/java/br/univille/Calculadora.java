package br.univille;

public class Calculadora {

    public int soma(int num1, int num2){
        return num1 + num2;
    }

    public int subtracao(int i, int j) {
        return i - j;
    }

    public float divisao(int i, int j){
        return i / j;
    }

    public float divisao(float i, float j){
        return i / j;
    }
    
}
